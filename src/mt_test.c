void mt_test_tree()
{
  mt_t *mt = mt_create();
  mt_print(mt);
  for (uint32_t i = 0; i < D_TEST_VALUES; ++i) {
    mt_add(mt, test_values[i], HASH_LENGTH);
  }

  mt_print(mt);

  for (uint32_t i = D_TEST_VALUES; i > 0; --i) {
    if (mt_verify(mt, test_values[i], HASH_LENGTH, i) == MT_ERR_ROOT_MISMATCH) {
      printf("Root mismatch error!\n");
      return;
    }
  }
  mt_delete(mt);
}

